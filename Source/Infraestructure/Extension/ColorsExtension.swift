import Foundation
import UIKit

extension UIColor {
    static var inputsColor: UIColor {
        return UIColor(named: "inputsColor")!
    }
    
    static var outputsColor: UIColor {
        return UIColor(named: "outputsColor")!
    }
    
    static let background = UIColor(red: 255/255, green: 254/255, blue: 235/255, alpha: 1)
}
